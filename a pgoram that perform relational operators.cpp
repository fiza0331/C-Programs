#include<iostream>
using namespace std;
int main()
{
	int x = 2;
	int y = 3;
	if(x>y)
	{
		cout<<x<<">"<<y<<endl;
	}
	if(x>=y)
	{
		cout<<x<<">="<<y<<endl;
	}
	if(x<y)
	{
		cout<<x<<"<"<<y<<endl;
	}
	if(x<=y)
	{
		cout<<x<<"<="<<y<<endl;
	}
	if(x==y)
	{
		cout<<x<<"="<<y<<endl;
	}
	if(x!=y)
	{
		cout<<x<<"!="<<y<<endl;
	}
}
